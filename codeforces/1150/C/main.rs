fn print_ans(a: &Vec<usize>) {
    for &v in a.iter() {
        print!("{} ", v);
    }
    println!();
}

fn main() {
    input!{
      n: usize,
      aa: [usize; n],
    }
    let mut count = vec![0; 3];
    for i in 0..n {
        count[aa[i]] += 1;
    }
    if count[1] == 0 || count[2] == 0 {
        return print_ans(&aa);
    }
    print!("{} {} ", 2, 1);
    count[2] -= 1;
    count[1] -= 1;

    let k = count[1]/2*2;
    for i in 0..k {
        print!("{} ", 1);
    }
    for i in 0..count[2] {
        print!("{} ", 2);
    }
    if count[1]%2==1 {
        print!("{} ", 1);
    }
}
