use std::cmp::{min, max};

#[allow(unused_imports)]
use std::io::Write;

fn main() {
    input!{
      n: usize,
      q: usize,
      aa: [usize; n],
      xs: [usize; q],
    }

    let mut s0 = vec![0; n+1];
    for i in 0..n {
        s0[i+1] = s0[i] + aa[i];
    }

    let mut s1 = vec![0; n+2];
    for i in 0..n+2 {
        if i+2 <= n+1 {
            s1[i+2] = s1[i] + aa[i];
        }
    }

    let mut s = vec![0; n];
    for i in 1..(n/2+2) {
        let mut sum = s0[n] - s0[n-i];
        if 2*i < n {
            sum += s1[n+1-2*i];
        }
        s[i] = sum;
    }
    // debug!(s);
}
