// [l, r]
fn binary_search<F>(mut l: i64, mut r: i64, f: &F) -> Option<i64>
  where F: Fn(i64) -> bool
  {
    assert!(l <= r);
    r += 1;
    let r_bound = r;
    while r > l {
        let m = l + (r - l) / 2;  // avoid overflow
        if f(m) {
          r = m;
        } else {
          if l == m { break; }
          l = m;
        }
    }
    if r == r_bound { None } else { Some(r) }
}

